def send_card(title, content, template="blue"):
    card = {
        "config": {"wide_screen_mode": True},
        "header": {"title": {"content": title[:30], "tag": "plain_text"}, "template": template},
        "elements": [{"tag": "markdown", "content": content}],
    }
    card_json = json.dumps(card, ensure_ascii=False)
    if len(card_json) > 28000:
        card["elements"] = [{"tag": "markdown", "content": content[:5000] + "\n\n...(已截断)"}]
        card_json = json.dumps(card, ensure_ascii=False)
    resp = requests.post(
        'https://open.feishu.cn/open-apis/im/v1/messages?receive_id_type=chat_id',
        headers={'Authorization': f'Bearer {token}', 'Content-Type': 'application/json; charset=utf-8'},
        json={'receive_id': chat_id, 'msg_type': 'interactive', 'content': card_json}
    )
    r = resp.json()
    ok = r.get('code') == 0
    print(f"{'OK' if ok else 'FAIL'} {title[:30]} - {r.get('msg')}")


